; ======================================
; INT_Null - empty interrupt
; ======================================
INT_Null:
    rte

; ======================================
; INT_Exception - CPU error
; ======================================
INT_Exception:
    DBG_RAISE_ERROR "INT_Exception"
    rte

; ======================================
; INT_VBlank - Vertical blank
; ======================================
INT_VBlank:
    addq.l #0x1, RAM_VBLANK_COUNTER
    rte

; ======================================
; INT_HBlank - Horizontal blank
; ======================================
INT_HBlank:
    rte